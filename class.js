//Class
class Person {
    constructor(name, age) {
        this.name = name;
        this.age = age;
    }
}

let person = new Person('Alice', 14);
console.log(person.age);

//class is a keyword in JavaScript that is used to define a new class. It was introduced in ECMAScript 2015 (also known as ES6) and provides a new syntax for creating classes and defining their methods.

//The class keyword allows us to define an object-oriented class with a constructor method and other methods.
